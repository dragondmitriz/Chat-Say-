        Form.jProgressBar1.setValue(100);
        Form.jProgressBar1.setString(Integer.toString(100)+"%");
        boolean file_error=true;
        boolean stream=true;
        try{
            JFileChooser selectfile=new JFileChooser();
            int select=selectfile.showOpenDialog(selectfile);
            File crach=new File(selectfile.getSelectedFile().getName());
            if(select==0){
                File file=selectfile.getSelectedFile();
                crach.createNewFile();
                crach.delete();
                file_error=false;
                String str=null;
                if(Say_world){
                    str="World";
                }            
                else{
                    String ip_target=null;
                    for(Client client: clients){
                        if(client.getName().equals(jLabel1.getText())){
                            ip_target=client.getIp();
                        }
                    }
                    String ip_me=null;
                    Socket ip_of_server=new Socket(server,16661);
                    ObjectInputStream ip_stream=new ObjectInputStream(ip_of_server.getInputStream());
                    ip_me=(String)ip_stream.readObject();
                    Socket directory=new Socket(server,47774);
                    ObjectOutputStream directory_out=new ObjectOutputStream(directory.getOutputStream());
                    str=ip_me+" "+ip_target;
                    directory_out.writeObject(str);
                    ObjectInputStream directory_in=new ObjectInputStream(directory.getInputStream());
                    str=(String)directory_in.readObject();
                }
                FileInputStream fis=new FileInputStream(file);
                Socket s=new Socket(server,47772);
                ObjectOutputStream oos=new ObjectOutputStream(s.getOutputStream());
                oos.writeObject(new File(str+"\\"+file.getName()));
                while(fis.available()>0){
                    byte b[]=new byte[1];
                    fis.read(b);
                    oos.writeObject(b);
                }
                oos.close();
                fis.close();
                stream=false;
            }
        }catch(Exception e){
            if(file_error)
                JOptionPane.showMessageDialog(this, "Ошибка выбора файла");
            if(stream)
                JOptionPane.showMessageDialog(this, "Ошибка передачи файла");
        }
        Form.jProgressBar1.setValue(0);
        Form.jProgressBar1.setString(Integer.toString(0)+"%");
        jPanel5.setVisible(false);
        jPanel1.setVisible(true);
