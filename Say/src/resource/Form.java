    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        Form.jProgressBar1.setValue(100);
        Form.jProgressBar1.setString(Integer.toString(100)+"%");
        FileOutputStream fos=null;
        boolean stream=true;
        try{
            String path;
            JFileChooser selectfile=new JFileChooser();
            int select=selectfile.showOpenDialog(selectfile);
            if(select==0){
                if(selectfile.getSelectedFile().isDirectory()){
                    path=selectfile.getSelectedFile().getName()+"\\"+(String)jList1.getSelectedValue();
                }
                else{
                    path=selectfile.getSelectedFile().getParent()+"\\"+(String)jList1.getSelectedValue();
                }                
                File file=new File(path);
                file.createNewFile();
                file=new File(path);
                fos=new FileOutputStream(file);
                Socket s=new Socket(server,47773);
                ObjectOutputStream oos=new ObjectOutputStream(s.getOutputStream());
                oos.writeObject(list[jList1.getSelectedIndex()]);            
                ObjectInputStream ois=new ObjectInputStream(s.getInputStream());           
                try{
                    s.setSoTimeout(5000);
                    while(true){
                        fos.write((byte[])ois.readObject());
                    }
                }catch(SocketTimeoutException e){}
                ois.close();
                stream=true;
            }   
        }catch(HeadlessException | IOException | ClassNotFoundException e){
            if(stream)
                JOptionPane.showMessageDialog(this, "Ошибка передачи файла");
        }
        try{
            fos.close();
        }catch(Exception e){}
        Form.jProgressBar1.setValue(0);
        Form.jProgressBar1.setString(Integer.toString(0)+"%");
        jPanel5.setVisible(false);
        jPanel1.setVisible(true);
    }//GEN-LAST:event_button1ActionPerformed

